package com.exam.store.controller.dto;

import javax.validation.constraints.NotNull;

public class CategoryDTO {
    private Long id;
    @NotNull
    private String name;

    public CategoryDTO() {
    }

    public CategoryDTO(Long id, String name) {
        this.id = id;
        this.name = name;
    }

    public Long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
